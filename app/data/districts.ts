// Получить центр по району
export function getCenterForDistrict(district: string): string | undefined {
  const mapping = DISTRICT_MAPPINGS.find(m => m.district === district);
  return mapping?.centerName;
}

// Получить все районы для центра
export function getDistrictsForCenter(centerName: string): string[] {
  return DISTRICT_MAPPINGS
    .filter(m => m.centerName === centerName)
    .map(m => m.district);
}